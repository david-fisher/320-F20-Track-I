CREATE TABLE students(
	ID 		INTEGER 	NOT NULL,
	Name 		VARCHAR(70) 	NOT NULL,
	COURSES		INTEGER[],
	PRIMARY KEY(ID)
);
CREATE TABLE demographics(
	ID	INTEGER REFERENCES students(ID)
	AGE	INTEGER,
	GRADE	VARCHAR(3),
	GENDER	TEXT,
	RACE	TEXT,
	MAJOR	VARCHAR(70),
	PRIMARY KEY(ID)
);
CREATE TABLE professors(
	ID	INTEGER 	NOT NULL,
	NAME	VARCHAR(70)	NOT NULL,
	COURSES	INTEGER[],
	PRIMARY KEY(ID)
);
CREATE TABLE courses(
	ID 	INTEGER		NOT NULL,
	NAME	VARCHAR(70)	NOT NULL,
	SCENARIOS	INTEGER[],
	PRIMARY KEY(ID)
);
CREATE TABLE responses(
	S_ID		INTEGER	REFERENCES students(ID),
	P_ID		INTEGER REFERENCES professors(ID),
	/* E_ID		INTEGER REFERENCES simulations(ID), Reliant on the scenario editor having an ID field */
	C_ID 		INTEGER REFERENCES courses(ID),
	DATE 		DATE	NOT NULL,
	REFLECTIONS	TEXT[],
	CHOICE		TEXT,
	PEOPLE_TALKED	INTGER[],
	CONVOS_HAD	TEXT[],	
	SCORE		INTEGER[],
	PRIMARY KEY(S_ID, C_ID, DATE) /* E_ID would also go in, see comment above */	
);

